#Funções Matemáticas
min()       # Encontra o MENOR valor em uma sequência
max()       # Encontra o MAIOR valor em uma sequência  
sum()       # Soma TODOS os elementos de uma sequência
abs()       # Retorna o VALOR ABSOLUTO (remove sinal negativo)
round()     # ARREDONDA um número para inteiro ou casas decimais
pow()       # Calcula POTÊNCIA (número elevado a outro)
divmod()    # Retorna QUOCIENTE e RESTO da divisão

# Funções para Sequências
len()       # Retorna o TAMANHO/QUANTIDADE de elementos
sorted()    # Retorna uma NOVA LISTA ORDENADA
reversed()  # Retorna um ITERADOR com ordem INVERTIDA
enumerate() # Retorna ÍNDICE e VALOR de cada elemento
zip()       # COMBINA múltiplas sequências em pares
any()       # Retorna True se PELO MENOS UM elemento for True
all()       # Retorna True se TODOS os elementos forem True

#  Funções de Conversão
int()       # Converte para NÚMERO INTEIRO
float()     # Converte para NÚMERO DECIMAL  
str()       # Converte para TEXTO/STRING
bool()      # Converte para VALOR BOOLEANO (True/False)
list()      # Converte para LISTA
tuple()     # Converte para TUPLA
set()       # Converte para CONJUNTO (valores únicos)

# Outras Funções Úteis
min([5, 2, 8])          # 2
max([5, 2, 8])          # 8
sum([1, 2, 3])          # 6
len("Python")           # 6
abs(-10)                # 10
round(3.14159, 2)       # 3.14
any([False, True])      # True
all([True, True])       # True
